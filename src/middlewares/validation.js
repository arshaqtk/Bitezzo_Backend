


const validator = (schema) => (req, res, next) => {
  const { error } = schema.validate(req.body, { abortEarly: false });

  if (error) {
    const errors = error.details.map((err) => err.message);
    console.log(errors)
    return res.status(400).json({ errors });
  }

  next();
};




module.exports = { validator }